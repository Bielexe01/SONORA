create index if not exists playlists_user_created_idx
  on public.playlists (user_id, created_at desc);

create table if not exists public.ascensao_posts (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  title text,
  content text,
  post_kind text not null default 'video',
  media_url text not null,
  likes_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ascensao_posts_kind_check check (post_kind in ('video', 'audio', 'youtube'))
);

create index if not exists ascensao_posts_user_created_idx
  on public.ascensao_posts (user_id, created_at desc);

create table if not exists public.user_follows (
  id bigint generated by default as identity primary key,
  follower_id uuid not null references public.profiles(id) on delete cascade,
  following_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint user_follows_unique unique (follower_id, following_id),
  constraint user_follows_diff check (follower_id <> following_id)
);

create index if not exists user_follows_follower_idx
  on public.user_follows (follower_id);

create index if not exists user_follows_following_idx
  on public.user_follows (following_id);

