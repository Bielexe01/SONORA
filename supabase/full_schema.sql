create table if not exists public.moderation_reports (
  id bigint generated by default as identity primary key,
  reporter_id uuid not null references public.profiles(id) on delete cascade,
  reported_user_id uuid references public.profiles(id) on delete set null,
  target_type text not null,
  target_post_id text,
  target_profile_id text,
  target_listing_id text,
  reason text not null,
  details text,
  created_at timestamptz not null default now(),
  constraint moderation_reports_target_type_check check (
    target_type in ('post', 'profile', 'listing')
  ),
  constraint moderation_reports_exactly_one_target check (
    (
      case when target_post_id is not null then 1 else 0 end
      + case when target_profile_id is not null then 1 else 0 end
      + case when target_listing_id is not null then 1 else 0 end
    ) = 1
  ),
  constraint moderation_reports_target_matches_type check (
    (target_type = 'post' and target_post_id is not null and target_profile_id is null and target_listing_id is null)
    or (target_type = 'profile' and target_post_id is null and target_profile_id is not null and target_listing_id is null)
    or (target_type = 'listing' and target_post_id is null and target_profile_id is null and target_listing_id is not null)
  )
);

