create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  handle text not null,
  bio text,
  avatar_url text,
  cover_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_handle_format check (left(handle, 1) = '@')
);

create unique index if not exists profiles_handle_unique_ci
  on public.profiles (lower(handle));

create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  content text,
  spotify_url text,
  spotify_type text,
  media_url text,
  media_type text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists posts_user_id_created_at_idx
  on public.posts (user_id, created_at desc);

create table if not exists public.post_likes (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.posts(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint post_likes_unique unique (post_id, user_id)
);

create index if not exists post_likes_user_id_idx
  on public.post_likes (user_id);

create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.posts(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  content text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists comments_post_id_created_at_idx
  on public.comments (post_id, created_at asc);

create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  sender_id uuid not null references public.profiles(id) on delete cascade,
  receiver_id uuid not null references public.profiles(id) on delete cascade,
  content text not null,
  created_at timestamptz not null default now(),
  constraint messages_sender_receiver_diff check (sender_id <> receiver_id)
);

create index if not exists messages_sender_receiver_created_idx
  on public.messages (sender_id, receiver_id, created_at desc);

create index if not exists messages_receiver_sender_created_idx
  on public.messages (receiver_id, sender_id, created_at desc);

create table if not exists public.communities (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  description text,
  created_by uuid not null references public.profiles(id) on delete cascade,
  genre text,
  is_public boolean not null default true,
  avatar_url text,
  cover_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists communities_created_by_idx
  on public.communities (created_by);

create table if not exists public.community_members (
  id bigint generated by default as identity primary key,
  community_id uuid not null references public.communities(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint community_members_unique unique (community_id, user_id)
);

create index if not exists community_members_user_idx
  on public.community_members (user_id);

create table if not exists public.community_posts (
  id bigint generated by default as identity primary key,
  community_id uuid not null references public.communities(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  post_type text,
  title text,
  spotify_url text,
  image_url text,
  album text,
  track_number text,
  content text,
  likes_count integer not null default 0,
  shares_count integer not null default 0,
  saves_count integer not null default 0,
  comments jsonb not null default '[]'::jsonb,
  poll_options jsonb not null default '[]'::jsonb,
  poll_votes jsonb not null default '{}'::jsonb,
  poll_total_votes integer not null default 0,
  challenge_entries jsonb not null default '[]'::jsonb,
  challenge_unit text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint community_posts_comments_is_array check (jsonb_typeof(comments) = 'array'),
  constraint community_posts_poll_options_is_array check (jsonb_typeof(poll_options) = 'array'),
  constraint community_posts_poll_votes_is_object check (jsonb_typeof(poll_votes) = 'object'),
  constraint community_posts_challenge_entries_is_array check (jsonb_typeof(challenge_entries) = 'array')
);

create index if not exists community_posts_community_created_idx
  on public.community_posts (community_id, created_at desc);

create index if not exists community_posts_user_created_idx
  on public.community_posts (user_id, created_at desc);

create table if not exists public.playlists (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  name text not null,
  description text,
  spotify_url text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
