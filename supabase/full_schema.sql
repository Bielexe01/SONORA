create or replace function public.is_community_owner(community uuid, user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.communities c
    where c.id = community
      and c.created_by = user_id
  );
$$;

create or replace function public.is_community_approved_member(community uuid, user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    public.is_community_owner(community, user_id)
    or exists (
      select 1
      from public.community_members cm
      where cm.community_id = community
        and cm.user_id = user_id
        and cm.status = 'approved'
    );
$$;

create or replace function public.is_community_manager(community uuid, user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    public.is_community_owner(community, user_id)
    or exists (
      select 1
      from public.community_members cm
      where cm.community_id = community
        and cm.user_id = user_id
        and cm.status = 'approved'
        and cm.role in ('admin', 'mod')
    );
$$;

create or replace function public.can_edit_community_settings(community uuid, user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    public.is_community_owner(community, user_id)
    or exists (
      select 1
      from public.community_members cm
      where cm.community_id = community
        and cm.user_id = user_id
        and cm.status = 'approved'
        and cm.role = 'admin'
    );
$$;

