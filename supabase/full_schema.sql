create or replace function public.media_object_belongs_to_user(object_name text)
returns boolean
language sql
stable
as $$
  select
    split_part(object_name, '/', 1) = auth.uid()::text
    or split_part(object_name, '/', 2) = auth.uid()::text;
$$;

